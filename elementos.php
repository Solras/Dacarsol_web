							<h2>Elementos de un juego de mesa</h2>		
						</header>
						<div class="content">
							<p>Cada vez que jugamos un juego de mesa, nos encontramos con una experiencia creada por un diseñador. Conforme nos adentramos en cada juego, es interesante darnos cuenta de los patrones que encontramos. Cosas que parecen familiares o que cosas que son comunes en los juegos de mesa. Entender estos «elementos» nos ayuda a entender lo que nos gusta o no nos gusta de los juegos de mesa. Y esto hace que nos conozcamos más como jugadores.</p>
							<p>Aquí es donde entramos en un terreno complicado. Aunque los juegos de mesa están contenidos en una caja, la experiencia tiene muchas partes intangibles. ¿Qué ideas intangibles componen un juego de mesa? Vamos a intentar analizar un poco algunos dominios comunes entre los juegos de mesa.</p>
							
							<h2>Meta</h2>
							<p>Casi cualquier juego de mesa tiene una meta de algún tipo. Las metas pueden ser a manera de carrera (el primero en llegar a X puntos), competencia en un tiempo limitado (el mejor puntaje en X rondas o en X tiempo), o referirse a la eliminación de los oponentes (el último jugador gana).</p>
							<p>En la mayoría de las veces encontramos que la regla está relacionada al final del juego.</p>

							<h2>Acciones</h2>
							<p>Los jugadores deben poder tomar acciones en el juego. Si el juego no les permite tomar acciones, se puede tratar más de una simulación. Las acciones pueden tomar un sin número de formas distintas. Pueden depender del sistema de juego que el diseñador creó o de otros jugadores y sus interacciones.</p>
							<p>Y no es sólo el qué pueden hacer sino el cómo pueden hacerlo que es importante. No es lo mismo tomar acciones simultáneas, que se modifique el orden de turno o que se juegue en un turno fijo todo el juego.</p>
							
							<h2>Recursos</h2>
							<p>Aunque existen juegos que parece que no tienen «recursos» como tal, lo más probable es que tengan un manejo de recursos abstractos, como la cantidad de turnos o el tiempo real. Simplemente el decidir cómo usar un turno es un recurso.</p>
							<p>Aún así, los juegos que tienen un sistema de recursos más atado a un sistema económico pueden ser un gran añadido para un juego. Ayuda a controlar el ritmo de juego, crear diferentes estrategias e incluso facilitar la interacción entre jugadores.</p>

							<h2>Adquisición</h2>
							<p>Algunos juegos se concentran en adquirir elementos. Desde juegos básicos como cualquier Memorama o Lince hasta juegos como Splendor o Targi.</p>
							<p>Ésta parte de los juegos de mesa permite a los jugadores sentir que están creando algo y les hace sentir que han logrado algo relevante en el juego.</p>

							<h2>Puntaje</h2>
							<p>Quizás uno de los elementos que parece más lógico. Pero hay tantas maneras distintas de hace puntajes. Y esto puede afectar mucho la manera de jugar o la experiencia de juego. Especialmente en los juegos que son netamente de adquisición o centrados en adquisición.</p>
							<p>La verdad es que hay sistemas de puntaje que hacen o deshacen un juego. Y existen algunos que incluso son difíciles de explicar pero que se convierten en una genialidad. Un buen sistema de puntaje o un puntaje que agregue algo novedoso puede hacer que un buen juego se convierta en un gran juego.</p>

							<h2>Eliminación</h2>
							<p>No solo hablando de eliminación de jugadores. Sino también de eliminación de adquisiciones, de piezas o de elementos de juego (como en el ajedrez). Y en algunos juegos, especialmente en aquellos que se alejan de los euros o juegos más viejitos, encontramos eliminación directa de jugadores.</p>
							<p>En tiempos modernos hemos encontrado que los diseñadores están abandonando esta práctica porque hace que la experiencia para los jugadores eliminados no sea la mejor. Aunque hay juegos que debido a su duración, logran hacer buen uso de la eliminación de jugadores.</p>

							<h2>Incertidumbre</h2>
							<p>La incertidumbre o alguna forma de azar es un elemento muy usado en los juegos de mesa. Y va más allá de los elementos de azar que normalmente relacionamos con el término, como tirar dados o barajar un mazo de cartas.</p>
							<p>Aunque un mecanismo de incertidumbre puede ser también una mano de un jugador que se mantiene en secreto, o el simple hecho de no saber las intenciones de los otros jugadores.</p>

							<h2>Interacción</h2>
							<p>Quizás uno de los elementos que más atrapan a las personas al hobby es la parte de la interacción. Y aunque bien, es cierto que existen grandes juegos en solitario, pero la experiencia de compartir juegos de mesa con alguien más, sentarse cara a cara y divertirse es simplemente única.</p>
							<p>La manera en que los jugadores interactúan en los juegos es tan variada como diseñadores de juegos y mecánicas hay. Desde interacciones indirectas, interacciones directas, juegos de deducción social con acusaciones o en los que el juego está encima de la mesa. Podemos tener conflicto, cooperación, obstrucción, subasta, traición y muchas más.</p>
